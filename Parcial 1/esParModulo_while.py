# Codigo para determinar si un número es par o es impar usando móudulo

while(True):
    # Entrada
    n = float(input("Ingrese un número : "))

    # Procesos
    if n % 2 == 0:
        print("ES PAR") # Salida
    else:
        print("ES IMPAR") # Salida
    print(".............................................")